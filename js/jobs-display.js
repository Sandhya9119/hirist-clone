        building_div.setAttribute("class","building");
        
        var building_icon = document.createElement("i");
        building_icon.setAttribute("class","fa fa-building-o");
        building_div.append(building_icon);

        var job_card = document.createElement("div");
        job_card.setAttribute("class", "job-card");

        var job_des = document.createElement("div");

        var job_name = document.createElement("div");

        var job_link = document.createElement("a");
        job_link.innerText = element.job_name;
        job_link.setAttribute("href",element.job_href);

        job_name.append(job_link);

        var job_details = document.createElement("div");
        job_details.setAttribute("class","job-details");

        var div_icon = document.createElement("div");
        var suit_icon = document.createElement("i");
        suit_icon.setAttribute("class","fa fa-briefcase");

        div_icon.append(suit_icon,element.jpb_company);

        var job_exp = document.createElement("div");
        job_exp.innerText = element.job_experience;

        var job_loc = document.createElement("div");
        job_loc.innerText = element.job_location;

        var job_date = document.createElement("div");
        job_date.innerText = element.job_date;

        job_details.append(div_icon,dot,job_exp,dot2,job_loc,dot3,job_date);

        job_des.append(job_name,job_details);

        var icons_div = document.createElement("div");
        icons_div.setAttribute("class","icons");

        var outer_div = document.createElement("div");
        var inner_div = document.createElement("div");
        var icon1 = document.createElement("i");
        icon1.setAttribute("class","fa fa-bookmark");
        inner_div.appendChild(icon1);
