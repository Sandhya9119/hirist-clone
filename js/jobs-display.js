        outer_div.append(inner_div,p);

        var outer_div2 = document.createElement("div");
        var inner_div2 = document.createElement("div");
        var icon2 = document.createElement("i");
        icon2.setAttribute("class","fa fa-info");
        inner_div2.appendChild(icon2);
