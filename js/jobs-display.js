        inner_div3.appendChild(icon3);
        var p3 = document.createElement("p");
        p3.innerText = "Save";
        outer_div3.append(inner_div3,p3);

        icons_div.append(outer_div,outer_div2,outer_div3);

        job_card.append(job_des,icons_div);

